data = input("Enter something: ")

val_int   = int(data)
val_float = float(data)
val_bool  = bool(data)
val_str   = str(data)


print("Integer:", val_int, type(val_int))
print("Float:  ", val_float, type(val_float))
print("Boolean:", val_bool, type(val_bool))
print("String: ", val_str, type(val_str))